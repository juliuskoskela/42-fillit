#include "bitlib.h"

int				bf_cmp(t_field *f1, t_field *f2)
{
	size_t		i;

	i = 0;
	while (i < f1->h)
	{
		if (f1->row[i] != f2->row[i])
			return (0);
		i++;
	}
	return (1);
}
