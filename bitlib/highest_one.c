#include "bitlib.h"

int				highest_one(unsigned int nb, size_t size)
{
	size_t		i;

	i = size - 1;
	while (i > 0)
	{
		if (((nb >> i) & 1) == 1)
			return(i);
		i--;
	}
	return(0);
}
