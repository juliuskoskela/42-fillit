#include "bitlib.h"

void			printbits(unsigned int nb, size_t size)
{
    size_t        i;
    char        c;

    i = 0;
    while (i < (size - 1))
    {
        c = checkbit(nb, i) ? '1' : '0';
        write(1, &c, 1);
        i++;
    }
}
